class 電影:
    def __init__(self, 標題, 類型, 上映年份):
        self.標題 = 標題
        self.類型 = 類型
        self.上映年份 = 上映年份

    def 顯示資訊(self):
        print(f"標題：{self.標題}")
        print(f"類型：{self.類型}")
        print(f"上映年份：{self.上映年份}")

    def 播放預告片(self):
        print(f"播放 {self.標題} 的預告片")

    def 取得年齡(self, 當前年份):
        年齡 = 當前年份 - self.上映年份
        return f"{self.標題} 已經上映 {年齡} 年了。"

# 建立三個物件
電影1 = 電影("盜夢空間", "科幻", 2010)
電影2 = 電影("黑暗騎士", "動作", 2008)
電影3 = 電影("鐵達尼號", "愛情", 1997)

# 呼叫
電影1.顯示資訊()
電影1.播放預告片()
print(電影1.取得年齡(2024))

print("\n")

電影2.顯示資訊()
電影2.播放預告片()
print(電影2.取得年齡(2024))

print("\n")

電影3.顯示資訊()
電影3.播放預告片()
print(電影3.取得年齡(2024))
